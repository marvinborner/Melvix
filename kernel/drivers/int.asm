%macro INT_REGISTER 1
dd int%1
%endmacro

%macro INT_ERR 1
int%1:
	push %1
	jmp int_common
%endmacro

%macro INT_NOERR 1
int%1:
	push 0
	push %1
	jmp int_common
%endmacro

%macro INT_SYSCALL 1
int%1:
	push 0
	push %1
	jmp int_common
%endmacro

extern int_handler
int_common:
	cld

	pushad
	push ds
	push es
	push fs
	push gs

	mov ax, 0x10
	mov ds, ax
	mov es, ax
	mov fs, ax
	mov gs, ax

	push esp
	call int_handler
	mov esp, eax

	pop gs
	pop fs
	pop es
	pop ds
	popad

	add esp, 8
	iret

